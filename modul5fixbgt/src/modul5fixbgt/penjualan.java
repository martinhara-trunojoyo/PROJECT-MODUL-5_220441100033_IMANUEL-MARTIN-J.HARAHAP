    private void cmbitemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbitemActionPerformed
        // TODO add your handling code here:
      // TODO add your handling code here:
    String selectedBarang = cmbitem.getSelectedItem().toString();

    try (Connection connection = (Connection) modul5fixbgt.koneksiDatabase.getConnection();
         PreparedStatement st = (PreparedStatement) connection.prepareStatement("SELECT kode_barang, harga_barang FROM barang WHERE nama_barang = ?")) {

        st.setString(1, selectedBarang);

        try (ResultSet rs = st.executeQuery()) {
            if (rs.next()) {
                // Set the price in the tfHarga_Barang text field
                kode_item.setText(rs.getString("kode_barang"));
                harga_item.setText(rs.getString("harga_barang"));
            } else {
                // Handle the case where the item was not found in the database
                harga_item.setText("Item not found");
            }
        }
   
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }

    }//GEN-LAST:event_cmbitemActionPerformed

