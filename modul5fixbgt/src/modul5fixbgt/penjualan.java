     private void populateComboBox() {
        try {
            Statement st = (Statement) modul5fixbgt.koneksiDatabase.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT nama_barang FROM barang");
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            model.addElement("Pilih Barang"); 
            while (rs.next()) {
                String namaBarang = rs.getString("nama_barang");
                model.addElement(namaBarang);
            }
            cmbitem.setModel(model);
            st.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }

    public void tampil_data(){
            DefaultTableModel tabel = new DefaultTableModel();
            tabel.addColumn("Barang");
            tabel.addColumn("Kode");
            tabel.addColumn("Harga");


            try {
                java.sql.Connection connection = modul5fixbgt.koneksiDatabase.getConnection();
                String sql = "select * from barang";
                java.sql.PreparedStatement pst = connection.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();

                while (rs.next()) {
                    Object[] row = {rs.getString(1), rs.getString(2),rs.getString(3),};
                    tabel.addRow(row);
                }

                table.setModel(tabel);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
